(function($) {

 	var menu, shadow, trigger, content, hash, currentTarget;
  var defaults = {
    menuStyle: {
      listStyle: 'none',
      padding: '1px',
      margin: '0px',
      backgroundColor: '#fff',
      border: '1px solid #999',
      width: '100px'
    },
    itemStyle: {
      margin: '0px',
      color: '#000',
      display: 'block',
      cursor: 'default',
      padding: '3px',
      border: '1px solid #fff',
      backgroundColor: 'transparent'
    },
    itemHoverStyle: {
      border: '1px solid #0a246a',
      backgroundColor: '#b6bdd2'
    },
    eventPosX: 'pageX',
    eventPosY: 'pageY',
    shadow : true,
    onContextMenu: null,
    onShowMenu: null
 	};

  $.fn.contextMenu = function(id, options) {
    if (!menu) {                                      // Create singleton menu
      menu = $('<div id="jqContextMenu"></div>')
               .hide()
               .css({position:'absolute', zIndex:'99000'})
               .appendTo('body')
               .bind('click', function(e) {
                 e.stopPropagation();
               });
    }
    if (!shadow) {
      shadow = $('<div></div>')
                 .css({backgroundColor:'#000',position:'absolute',opacity:0.2,zIndex:98999})
                 .appendTo('body')
                 .hide();
    }
    hash = hash || [];
    hash.push({
      id : id,
      menuStyle: $.extend({}, defaults.menuStyle, options.menuStyle || {}),
      itemStyle: $.extend({}, defaults.itemStyle, options.itemStyle || {}),
      itemHoverStyle: $.extend({}, defaults.itemHoverStyle, options.itemHoverStyle || {}),
      bindings: options.bindings || {},
      shadow: options.shadow || options.shadow === false ? options.shadow : defaults.shadow,
      onContextMenu: options.onContextMenu || defaults.onContextMenu,
      onShowMenu: options.onShowMenu || defaults.onShowMenu,
      eventPosX: options.eventPosX || defaults.eventPosX,
      eventPosY: options.eventPosY || defaults.eventPosY
    });

    var index = hash.length - 1;
    $(this).bind('contextmenu', function(e) {
      // Check if onContextMenu() defined
      var bShowContext = (!!hash[index].onContextMenu) ? hash[index].onContextMenu(e) : true;
      if (bShowContext) display(index, this, e, options);
      return false;
    });
    return this;
  };

  function display(index, trigger, e, options) {
    var cur = hash[index];
    content = $('#'+cur.id).find('ul:first').clone(true);
    content.css(cur.menuStyle).find('li').css(cur.itemStyle).hover(
      function() {
        $(this).css(cur.itemHoverStyle);
      },
      function(){
        $(this).css(cur.itemStyle);
      }
    ).find('img').css({verticalAlign:'middle',paddingRight:'2px'});

    // Send the content to the menu
    menu.html(content);

    // if there's an onShowMenu, run it now -- must run after content has been added
		// if you try to alter the content variable before the menu.html(), IE6 has issues
		// updating the content
    if (!!cur.onShowMenu) menu = cur.onShowMenu(e, menu);

    $.each(cur.bindings, function(id, func) {
      $('#'+id, menu).bind('click', function(e) {
        hide();
        func(trigger, currentTarget);
      });
    });
    var menu_height = menu.css('height');
    menu_height = menu_height.substring(0, menu_height.length-2);
    menu_height = Number(menu_height);
    
    var menu_width = menu.css('width');
    menu_width = menu_width.substring(0, menu_width.length-2);
    menu_width = Number(menu_width);
    
    var height=0;
    var width=0;
    //控制显示范围，当菜单靠近右边界时，显示在左边，靠近下边界时显示在上边(需要gantt.gantt_height,gantt.gantt_width指定范围)
    if(gantt&&gantt.gantt_height){
    	height = gantt.gantt_height;
    	width = gantt.gantt_width;
    }
    
    var top=e[cur.eventPosY];
    var left = e[cur.eventPosX];
    
    //log.warn('-----contextmenu top='+e[cur.eventPosY]+'--menu-height='+menu_height+'--gantt-height='+height+'  '+(top+menu_height));
    //log.warn('-----contextmenu left='+e[cur.eventPosX]+'--menu-width='+menu_width+'--gantt-width='+width+'  '+(left+menu_width));
   
    if(height>0 && top+menu_height>height){
    	top = top -menu_height;
    }
    if(width>0 && left+menu_width>width){
    	left = left -menu_width;
    }
    menu.css({'left':left,'top':top}).show();
    if (cur.shadow){ 
    	shadow.css({width:menu.width(),height:menu.height(),left:left+2,top:top+2}).show();
    }
    $(document).one('click', hide);
  }

  function hide() {
    menu.hide();
    shadow.hide();
  }
  
  function destroy(){
	  alert('=========');
  }

  // Apply defaults
  $.contextMenu = {
    defaults : function(userDefaults) {
      $.each(userDefaults, function(i, val) {
        if (typeof val == 'object' && defaults[i]) {
          $.extend(defaults[i], val);
        }
        else defaults[i] = val;
      });
    }
  };

})(jQuery);

$(function() {
  $('div.contextMenu').hide();
});